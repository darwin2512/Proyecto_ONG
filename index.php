        <h4>Infraestructura Pesquera</h4>
        <p>No existe infraestructura pesquera, sin embargo se observo un número considerable de muelles privados. Generalmente son construidos con madera Chico Zapote, en promedio tienen 1.5 mt de ancho por 40 mt de largo; no se descarta que en algunas ocasiones los utilicen pescadores para atracar y descargar los productos de la pesca.</p>
        <h4>Mercados Pesqueros</h4>
        <p>La comercialización se desarrolla en Río Dulce, Puerto Barrios, Zacapa, Chiquimula, Guatemala, todos estos mercados son de tipo urbano, el producto es transportado con hielo se vende fresco, al menudeo y al mayoreo. En época de semana santa se realizan actividades de transformación y procesamiento de productos pesqueros para ser comercializados en seco – salado.</p>
        <h4>Usos</h4>
        <p>Este cuerpo de agua se encuentra dentro de área protegida, por lo que su principal dedicación es para conservación y ecoturismo, realizándose también actividades de pesca.<strong> </strong></p>
        <h4>Institucionalidad<strong> </strong></h4>
        <p>En este cuerpo de agua actúan entre otros el Ministerio de Agricultura, Ganadería y Alimentación –MAGA-; a través de la Unidad de Manejo de la Pesca y Acuicultura -UNIPESCA- y la Coordinadora Departamental en Izabal. Ministerio de Ambiente y Recursos Naturales –MARN-; Consejo Nacional de Áreas Protegidas –CONAP-; Defensores de la Naturaleza, Co-administrador del área protegida. Capitanía de Puerto del Estor; Autoridad para el Manejo Sustentable de la Cuenca del Lago de Izabal.</p>
        <p>Dentro del Lago se llevó a cabo un programa de monitoreo y limpieza de <em>Hydrilla verticillata</em>, que estuvo a cargo del Ministerio de Ambiente y Recursos Naturales; el Consejo Nacional de Áreas Protegidas; el Ministerio de Agricultura, Ganadería y Alimentación. Este programa fue financiado por el Gobierno Central; Agencia Internacional Para el Desarrollo -USAID-.</p>
        <h4>Legislación y Normativas Presentes<strong> </strong></h4>
        <p>&#8211; Ley de Áreas Protegidas, Decreto del Congreso de la República de Guatemala 4-89; el cual fue publicado en el Diario Oficial el 10 de febrero de 1989 y su objetivo principal es manejo de las áreas protegidas.</p>
        <p>&#8211; Ley General de Pesca y Acuicultura, Decreto del Congreso de la República de Guatemala 80-2002; el cual fue publicado en el Diario Oficial el 24 de diciembre del 2002 y su objetivo principal es regular las actividades pesqueras y acuícolas.</p>
        <p>&#8211; Ley de Mejoramiento del Medio Ambiente, Decreto del Congreso de la República de Guatemala 68-86; el cual fue publicado en el Diario Oficial el 19 de diciembre de 1986 y su objetivo principal es la conservación y mejoramiento del medio ambiente.</p>
        <p>&#8211; Ley de Creación de la Autoridad para el Manejo Sustentable de la Cuenca del Lago de Izabal; Y de Río Dulce y su Cuenca. Decreto 10-98</p>
        <p>&#8211; Ley que Declara Área Protegida el Refugio de Vida Silvestre Bocas de Polochic. Decreto 38-96.</p>
        <h4>Datos Socioeconómicos</h4>
        <p>El total de la población de los municipios aledaños es de 109,910 habitantes de los cuales el 48 % son hombres y 52 % son mujeres. El Instituto Nacional de Estadística –INE- (2002), reporta que el ingreso familiar promedio para la región, donde se encuentra el Departamento de Izabal está distribuido de la siguiente manera: el 16.44% perciben un ingreso mensual de menos de US $ 100.00; el 49.79% reportan ingresos mensuales entre US $ 101.00 y 300.00; y el restante 33.76% perciben mensualmente más de US $ 300.00.</p>
        <p>El salario mínimo en todo el país para actividades agrícolas es de US $ 4.82 por una jornada diaria ordinaria de trabajo o por una tarea diaria de trabajo, según Acuerdo Gubernativo 765-2003.</p>
        <p>La mayoría de comunidades cuenta con servicio de agua potable, teléfono, luz eléctrica, escuela, puesto de salud y accesos por vía terrestre.</p>
        <p>Existen dos asociaciones de pescadores:</p>
        <p>&#8211; Asociación de Pescadores de Bocas del Polochic, con 125 asociados y sede en El Estor Izabal, no tiene personería jurídica.</p>
        <p>&#8211; Sindicato Gremial de Pescadores y Similares de Izabal, con 40 asociados y sede en El Estor, Izabal, si tiene personería jurídica.</p>
