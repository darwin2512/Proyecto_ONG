        <p>El nombre oficial para este cuerpo de agua es Lago de Izabal .</p>
        <p>Corresponde a la clasificación de Lago, se encuentra ubicado en los Municipios de El Estor, Los Amates, Morales y Livingston todos en el Departamento de Izabal, Guatemala. A una Latitud Norte de 15°24´02&#8243; y una Longitud Oeste de 89°20´00&#8243;a una elevación de 1 msnm.</p>
        <p><img class="aligncenter wp-image-3324 size-large" src="http://climapesca.org/wp-content/uploads/2017/05/gt-5-1024x705.png" alt="" width="750" height="516" srcset="http://climapesca.org/wp-content/uploads/2017/05/gt-5-300x207.png 300w, http://climapesca.org/wp-content/uploads/2017/05/gt-5-768x529.png 768w, http://climapesca.org/wp-content/uploads/2017/05/gt-5-1024x705.png 1024w, http://climapesca.org/wp-content/uploads/2017/05/gt-5.png 1096w" sizes="(max-width: 750px) 100vw, 750px" /></p>
        <h4>Datos Físicos</h4>
        <p>Este lago posee un espejo de agua de 590 km2 (Castañeda 1,995) .</p>
        <p>La profundidad promedio es de aproximadamente 4m y la máxima de 18m.</p>
        <h4>Calidad del Agua</h4>
        <p>Temperatura:<br />
        La temperatura media es de 28.09°C., lo que demuetra la importancia de la iluminación solar y su efecto térmico. (Basterrechea, 1993).</p>
        <p>Oxígeno Disuelto:<br />
        El valor medio en el lago y la columna de agua es de 6.76 mg/l, en terminos generales la columna de agua se mantiene bien oxígenada. (Basterrechea, 1993).</p>
        <p>pH:<br />
        El pH, se mantiene ligeramente alcalino los valoares oscilaron entre 7.25 y 7.72., en terminos generales este parámetro se mantiene estable en el lago y la columna de agua. (Basterrechea, 1993).</p>
        <p>Transparencia:<br />
        El valor medio de transparencia es de 2.75m. Probablemente este valor se representa de esta manera debido a la influencia de tributarios sobre el lago que sus aguas arrastran sólidos en suspensión. (Basterrechea, 1993).</p>
        <p>Sólidos Totales:<br />
        Los valores oscilaron entre 146 y 173 mg/l; el hecho es que se ven influnciados por la cantidad de escorrentias que bajan al lago durante la época seca y le época lluviosa. (Basterrechea, 1993).</p>
        <h4>Pesca y Acuicultura</h4>
        <p>La comunidad usuaria de este recurso la constituyen habitantes de las siguientes aldeas y caseríos: El Sauce, El Zapatillo, Punto Muerto, Cocales, Izabalito, Punta Brava, Playa Dorada, Boca Ancha, Los Limones, San Felipe y La Esperanza, ubicadas en el Departamento Izabal. En materia pesquera el número aproximado es de 525 pescadores quienes para realizar las actividades pesqueras emplean 285 pangas con motor; 302 pescadores utilizan 604 redes agalleras; 125 utilizan 15 chinchorros con método de arrastre, 93 utilizan atarrayas de 12 y 15 cuartas con una luz de malla menor de 0.5”, 70 pescadores utilizan arpón. La producción anual estimada es de 4, 720,000 Kg.</p>
        <h4>Acuicultura</h4>
        <p>Se determinó que dentro del lago no se realiza ninguna actividad acuícola, ni tampoco se utiliza el agua de la misma para abastecer total o parcialmente algún proyecto. Este cuerpo de agua presenta características para poder desarrollar proyectos acuícolas.</p>
